namespace Rabsanaco\Contracts\UI\Widgets;


abstract class Heading extends Container
{
    protected $size;

    /**
     * Heading constructor.
     * @param $size
     */
    public function __construct($size)
    {
        $this->size = $size;
    }

    /**
     * @return mixed
     */
    public function getSize()
    {
        return $this->size;
    }

    /**
     * @param mixed $size
     */
    public function setSize($size): void
    {
        $this->size = $size;
    }


}
