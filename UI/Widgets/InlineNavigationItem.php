namespace Rabsanaco\Contracts\UI\Widgets;


abstract class InlineNavigationItem extends NavigationItem
{
    protected $active = false;

    /**
     * @return bool
     */
    public function isActive(): bool
    {
        return $this->active;
    }

    /**
     * @param bool $active
     */
    public function setActive(bool $active): void
    {
        $this->active = $active;
    }




}
